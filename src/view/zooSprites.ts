export const ZOO_NAMES = ['鸭','鸟','马','猫','狐','狗','章鱼','蛇'];

const DESK = '#46506a'; // 工位家具(暗色,跟白动物拉开)

// 单个工位:在 w×H 槽位里铺桌面(底 1 行整宽)+ 右侧隔断(上半竖条),再把动物底部对齐叠上去。
function deskSlot(animal: Grid, H: number): Grid {
  const w = animal[0].length;
  const slot: (string | null)[][] = Array.from({ length: H }, () => new Array(w).fill(null));
  for (let x = 0; x < w; x++) slot[H - 1][x] = DESK;                 // 桌面
  for (let y = Math.max(0, H - 6); y < H; y++) slot[y][w - 1] = DESK; // 右隔断
  const pad = H - animal.length;                                     // 动物底部对齐叠上
  for (let y = 0; y < animal.length; y++) {
    for (let x = 0; x < w; x++) { const c = animal[y][x]; if (c) slot[pad + y][x] = c; }
  }
  return slot;
}

/**
 * 一排工位:活堆起来"加人",从 roster 连续取 n 只,每只一个工位(桌+隔断)并排(间隔 2px)。
 * n=1 即一个工位的单只(和轮播一致)。startIdx 决定从哪只开始(随时间轮换)。
 */
export function crewSprite(startIdx: number, n: number): Grid {
  const len = ZOO_ROSTER.length;
  const animals: Grid[] = [];
  for (let i = 0; i < Math.max(1, n); i++) animals.push(ZOO_ROSTER[(((startIdx + i) % len) + len) % len]);
  const H = Math.max(...animals.map((a) => a.length)) + 1; // +1 给桌面留行
  const GAP = 2;
  const rows: (string | null)[][] = Array.from({ length: H }, () => []);
  for (let ai = 0; ai < animals.length; ai++) {
    const slot = deskSlot(animals[ai], H);
    const w = slot[0].length;
    for (let y = 0; y < H; y++) {
      for (let x = 0; x < w; x++) rows[y].push(slot[y][x]);
      if (ai < animals.length - 1) for (let g = 0; g < GAP; g++) rows[y].push(null);
    }
  }
  return rows;
}
