/**
 * 一队动物并排(底部对齐、间隔 2px):活堆起来"加人",从 roster 连续取 n 只拼成一张宽 sprite。
 * n=1 即单只(和轮播一致)。startIdx 决定从哪只开始(随时间轮换)。
 */
export function crewSprite(startIdx: number, n: number): Grid {
  const len = ZOO_ROSTER.length;
  const animals: Grid[] = [];
  for (let i = 0; i < Math.max(1, n); i++) animals.push(ZOO_ROSTER[(((startIdx + i) % len) + len) % len]);
  const H = Math.max(...animals.map((a) => a.length));
  const GAP = 2;
  const rows: (string | null)[][] = Array.from({ length: H }, () => []);
  for (let ai = 0; ai < animals.length; ai++) {
    const a = animals[ai];
    const w = a[0].length;
    const pad = H - a.length;                // 底部对齐:矮的上面补空
    for (let y = 0; y < H; y++) {
      const src = y < pad ? new Array(w).fill(null) : a[y - pad];
      for (let x = 0; x < w; x++) rows[y].push(src[x]);
      if (ai < animals.length - 1) for (let g = 0; g < GAP; g++) rows[y].push(null);
    }
  }
  return rows;
}
